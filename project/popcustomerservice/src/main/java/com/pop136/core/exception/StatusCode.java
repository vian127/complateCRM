package com.pop136.core.exception;

public enum StatusCode {
	SUCCESS( 200 , "OK" ) ,
	ERROR( 1 , "ERROR" ) ,
	UNAUTH(100000,"Unauthorized!");
	private int code ;
	private String message ;
	StatusCode(int code , String message ){
		this.code = code ;
		this.message = message ;
	}
	public int getCode() {
		return code;
	}
	public String getMessage() {
		return message;
	}
}
