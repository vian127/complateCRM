public class WeekValue {
    private String sBeginDate;

    private String sEndDate;

    private String sWeekCaption;

    public String getSBeginDate() {
        return sBeginDate;
    }

    public void setSBeginDate(String beginDate) {
        sBeginDate = beginDate;
    }

    public String getSEndDate() {
        return sEndDate;
    }

    public void setSEndDate(String endDate) {
        sEndDate = endDate;
    }

    public String getSWeekCaption() {
        return sWeekCaption;
    }

    public void setSWeekCaption(String weekCaption) {
        sWeekCaption = weekCaption;
    }

}
